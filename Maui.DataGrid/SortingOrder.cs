namespace Maui.DataGrid;

/// <summary>
/// Represents the sorting orders for DataGrid.
/// </summary>
public enum SortingOrder
{
    /// <summary>
    /// No sorting.
    /// </summary>
    None = 0,

    /// <summary>
    /// Ascending order.
    /// </summary>
    Ascendant = 1,

    /// <summary>
    /// Descending order.
    /// </summary>
    Descendant = 2,
}
