namespace Maui.DataGrid;

/// <summary>
/// Represents the sorting orders for <see cref="DataGrid"/>.
/// </summary>
public enum SortingOrder
{
    /// <summary>
    /// No sorting order.
    /// </summary>
    None = 0,

    /// <summary>
    /// Ascending sorting order.
    /// </summary>
    Ascendant = 1,

    /// <summary>
    /// Descending sorting order.
    /// </summary>
    Descendant = 2,
}
